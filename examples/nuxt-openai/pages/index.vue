<script setup lang="ts">
import { useChat } from '@ai-sdk/vue';
import { computed } from 'vue';

const { error, input, status, handleSubmit, messages, reload, stop } =
  useChat();

const disabled = computed(() => status.value !== 'ready');
</script>

<template>
  <div class="flex flex-col w-full max-w-md py-24 mx-auto stretch">
    <div v-for="m in messages" :key="m.id" class="whitespace-pre-wrap">
      {{ m.role === 'user' ? 'User: ' : 'AI: ' }}
      {{ m.content }}
    </div>

    <div
      v-if="status === 'submitted' || status === 'streaming'"
      class="mt-4 text-gray-500"
    >
      <div v-if="status === 'submitted'">Loading...</div>
      <button
        type="button"
        class="px-4 py-2 mt-4 text-blue-500 border border-blue-500 rounded-md"
        @click="stop"
      >
        Stop
      </button>
    </div>

    <div v-if="error" class="mt-4">
      <div class="text-red-500">An error occurred.</div>
      <button
        type="button"
        class="px-4 py-2 mt-4 text-blue-500 border border-blue-500 rounded-md"
        @click="() => reload()"
      >
        Retry
      </button>
    </div>

    <form @submit="handleSubmit">
      <input
        class="fixed bottom-0 w-full max-w-md p-2 mb-8 border border-gray-300 rounded shadow-xl"
        v-model="input"
        placeholder="Say something..."
        :disabled="disabled"
      />
    </form>
  </div>
</template>
